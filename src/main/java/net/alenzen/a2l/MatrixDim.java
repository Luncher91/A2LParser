package net.alenzen.a2l;

public class MatrixDim {
	private Long xDim;
	private Long yDim;
	private Long zDim;

	public Long getxDim() {
		return xDim;
	}

	public void setxDim(Long xDim) {
		this.xDim = xDim;
	}

	public Long getyDim() {
		return yDim;
	}

	public void setyDim(Long yDim) {
		this.yDim = yDim;
	}

	public Long getzDim() {
		return zDim;
	}

	public void setzDim(Long zDim) {
		this.zDim = zDim;
	}
}
